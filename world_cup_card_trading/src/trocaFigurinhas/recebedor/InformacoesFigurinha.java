package trocaFigurinhas.recebedor;

public class InformacoesFigurinha {
	
	String nomeJogador;
	
	int raridadeMinima;
	
	int numeroFigurinha;

	public int getNumeroFigurinha() { return numeroFigurinha; }

	public void setNumeroFigurinha(int numeroFigurinha) { this.numeroFigurinha = numeroFigurinha; }

	public String getNomeJogador() { return nomeJogador; }

	public void setNomeJogador(String nomeJogador) { this.nomeJogador = nomeJogador; }

	public int getRaridadeMinima() { return raridadeMinima; }

	public void setRaridadeMinima(int raridadeMinima) { this.raridadeMinima = raridadeMinima; }

}
